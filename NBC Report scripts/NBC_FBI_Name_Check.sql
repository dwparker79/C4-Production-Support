fbi.FBI_RESPONSE_CODE||'^'||
fbi.RESPONSE_CODE||'^'||
TO_CHAR(fbi.RESPONSE_DT,'MM/DD/YYYY')
FROM 
IBS_AP_APPLICATION apl,
IBS_AP_APPLICANT applic,
IBS_WF_PROCESS_INST proc,
IBS_AP_FBI_RESPONSE fbi
WHERE 
apl.APP_ID = applic.APP_ID
AND apl.APP_ID = proc.PROCESS_INST_ID
AND apl.APP_ID = fbi.app_id-- DWP 2017/06/29 USCISC4-342: Excluding military, military spouse, and BCT cases
AND NVL(apl.MILITARY, 'N') <> 'Y'
AND NVL(apl.MILITARY_SPOUSE, 'N') <> 'Y'
AND NVL(apl.BCT_INDICATOR, 'N') <> 'Y'
AND apl.FORM_NBR = 'N400'
AND proc.rec_sc_loc_code = 'NBC'
AND proc.process_state in ('Active','Running','Suspended')
AND fbi.RESPONSE_TYPE_CODE = 'N'
AND fbi.RESPONSE_DT = (SELECT MAX(fbi1.RESPONSE_DT) 
                         FROM IBS_AP_FBI_RESPONSE fbi1
                	 WHERE fbi1.APP_ID = apl.APP_ID
                      	   AND fbi1.RESPONSE_TYPE_CODE = 'N'
			   AND fbi1.RESPONSE_DT > sysdate -30);
